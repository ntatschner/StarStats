'use client';

import { useEffect } from 'react';
import Link from 'next/link';
import type { Route } from 'next';
import { usePathname } from 'next/navigation';

interface NavItem {
  label: string;
  href: Route;
  match?: (pathname: string) => boolean;
  /**
   * Optional count rendered as a small badge after the label. Used
   * for surfacing inbound state (e.g. # of people sharing with you)
   * so the user notices without having to visit the page. Hidden
   * when undefined or 0 so the rail stays calm for users with no
   * activity.
   */
  badge?: number;
}

interface NavSection {
  title: string;
  items: NavItem[];
}

interface Props {
  handle: string | null;
  /**
   * Site-wide staff grants for the current user (e.g. `["moderator"]`,
   * `["admin"]`). Mirrored from `/v1/auth/me` into the session cookie
   * at sign-in. We only render the Admin link when the user holds at
   * least one role; the actual /admin route enforces the same check
   * server-side, so this is purely a UX gate.
   */
  staffRoles: string[];
  /**
   * Number of inbound shares (people who have shared their manifest
   * with the current user). Rendered as a badge on the Sharing nav
   * entry so users notice new shares without having to visit the
   * page. Optional — undefined or 0 hides the badge.
   */
  inboundShareCount?: number;
}

function buildNav(
  handle: string | null,
  staffRoles: string[],
  inboundShareCount: number,
): NavSection[] {
  const profileHref = (
    handle ? `/u/${encodeURIComponent(handle)}` : '/settings'
  ) as Route;
  const isStaff = staffRoles.length > 0;
  return [
    // Insights — the user's own data surfaces. Audit v2 §03 collapsed
    // the old Metrics/Uploads rail entries into Journey (types tab) and
    // Devices (activity tab); the old routes still exist as redirect
    // stubs (W1/W2) but no longer surface here.
    {
      title: 'Insights',
      items: [
        { label: 'Manifest', href: '/dashboard' },
        {
          label: 'Journey',
          // Typed routes manifest is regenerated by `next build`; until
          // that runs the new path isn't in `Route`. Cast away the
          // mismatch — same approach the codebase uses for other
          // dynamic / freshly-added paths (see LocationPill, donate).
          href: '/journey' as Route,
          match: (p) => p === '/journey' || p.startsWith('/journey/'),
        },
        {
          // Moved from Setup → Insights per audit v2 §03: devices live
          // in the same "your data" cluster as Manifest/Journey now.
          label: 'Devices',
          href: '/devices',
          match: (p) => p === '/devices' || p.startsWith('/devices/'),
        },
      ],
    },
    // Community — outward-facing surfaces (profile pages, org rosters,
    // user-submitted reference fixes).
    {
      title: 'Community',
      items: [
        {
          label: 'Profile',
          href: profileHref,
          match: (p) => p.startsWith('/u/'),
        },
        {
          label: 'Orgs',
          href: '/orgs',
          match: (p) => p === '/orgs' || p.startsWith('/orgs/'),
        },
        {
          // Typed routes manifest is regenerated by `next build`; until
          // that runs the new path isn't in `Route`. Same cast pattern
          // used for /journey above.
          label: 'Sharing',
          href: '/sharing' as Route,
          match: (p) => p === '/sharing' || p.startsWith('/sharing/'),
          badge: inboundShareCount > 0 ? inboundShareCount : undefined,
        },
        {
          label: 'Submissions',
          href: '/submissions',
          match: (p) => p === '/submissions' || p.startsWith('/submissions/'),
        },
      ],
    },
    // Account — Settings absorbs the 2FA panel inline (W2). Support
    // replaces the old "Donate" rail entry (audit v2 §03 rename); the
    // route is still /support, the page already exists.
    {
      title: 'Account',
      items: [
        {
          label: 'Settings',
          href: '/settings',
          match: (p) => p === '/settings',
        },
        {
          label: 'Support',
          // Same /support route the audit landed in W1's folder rename
          // (donate → support); cast for the typed-routes manifest lag.
          href: '/support' as Route,
          match: (p) => p === '/support' || p.startsWith('/support/'),
        },
      ],
    },
    // Staff section — only present when the user holds a moderator or
    // admin grant. The Admin route runs its own server-side gate so
    // this is purely a UX affordance.
    ...(isStaff
      ? [
          {
            title: 'Staff',
            items: [
              {
                label: 'Admin',
                href: '/admin' as Route,
                match: (p: string) => p === '/admin' || p.startsWith('/admin/'),
              },
            ],
          },
        ]
      : []),
  ];
}

function isActive(item: NavItem, pathname: string): boolean {
  if (item.match) return item.match(pathname);
  return pathname === item.href;
}

export function LeftRail({ handle, staffRoles, inboundShareCount = 0 }: Props) {
  const pathname = usePathname() ?? '';
  const sections = buildNav(handle, staffRoles, inboundShareCount);

  // Mobile drawer leaks across soft-navigations: clicking a nav link
  // closes the rail visually but leaves `body[data-drawer="open"]` set,
  // so the next page mounts with a "stuck open" rail. Clear on every
  // pathname change. Runs server-side too but `document` is gated.
  useEffect(() => {
    if (typeof document !== 'undefined') {
      delete document.body.dataset.drawer;
    }
  }, [pathname]);

  return (
    <aside className="ss-rail" aria-label="Primary navigation">
      {sections.map((section) => (
        <div key={section.title}>
          <div className="ss-rail-section">{section.title}</div>
          {section.items.map((item) => {
            const active = isActive(item, pathname);
            return (
              <Link
                key={item.href + item.label}
                href={item.href}
                className="ss-rail-item"
                data-active={active ? 'true' : undefined}
                style={{ textDecoration: 'none' }}
              >
                <span className="ss-rail-dot" aria-hidden="true" />
                <span style={{ flex: 1 }}>{item.label}</span>
                {item.badge !== undefined && item.badge > 0 && (
                  <span
                    className="ss-rail-badge"
                    aria-label={`${item.badge} new`}
                  >
                    {item.badge > 99 ? '99+' : item.badge}
                  </span>
                )}
              </Link>
            );
          })}
        </div>
      ))}
      <div className="ss-rail-footer">
        <a href="/auth/logout">Sign out</a>
      </div>
    </aside>
  );
}
